/* Инициализация скрипта */
if ( $('.rent').length > 0 ) {
	$('.plan div[data-floor]').each(function() {
		var current = $(this);
		current.css({
			'width': $(this).find('img').attr('width'),
			'height': $(this).find('img').attr('height'),
			'background': 'url("'+ $(this).find('img').attr('src')+'") no-repeat left top'
		});
		var r = Raphael($(this).attr('id'), $(this).find('img').attr('width'), $(this).find('img').attr('height')),
		attributes = {
			fill: 'transparent',
			opacity: 0,
			'stroke-width': 0
		},
		arr = new Array();
		var paths = eval('b'+current.attr('data-building')+'_f'+current.attr('data-floor'));
		for (var shop in paths) {
			var obj = r.path(paths[shop].path);
			obj.attr(attributes);
			arr[obj.id] = shop;
			obj.hover(
				function() {
					this.animate({
						fill: 'transparent'
					}, 0);
					var point = this.getBBox(0);
					current.append('<p class="bubble">'+paths[arr[this.id]].space+'</p>');
					current.find('.bubble').css({
						'left': point.x+point.width/2+'px',
						'top': point.y-55+'px',
						'margin-left': '-30px'
					});
				},
				function() {
					this.animate({
						fill: attributes.fill,
						stroke: attributes.stroke
					}, 0);
					current.find('.bubble').remove();
				}
			);
		}
	});
	$('.rent .plan ul li a').bind('click', function() {
		$(this).parents('.plan').children('div[data-floor="'+$(this).attr('href')+'"]').show().siblings('div[data-floor]').hide();
		$(this).parent().addClass('active').siblings().removeClass();
		return false;
	}).filter(':first').click();
	$('.rent .nav ul li a').bind('click', function() {
		$(this).parents('.rent').children('div[data-tab="'+$(this).attr('href')+'"]').show().siblings('div[data-tab]').hide();
		$(this).parent().addClass('active').siblings().removeClass();
		return false;
	}).filter(':first').click();
}